import SwiftUI

struct ContentView: View {
    @State private var showLogin = false
    
    var body: some View {
        VStack {
            Image(systemName: "globe")
                .imageScale(.large)
                .foregroundStyle(.tint)
            Text("Hello, world!")
            
            Button(action: {
                showLogin = true
            }) {
                Text("Go to Login")
                    .padding()
                    .background(Color.blue)
                    .foregroundColor(.white)
                    .cornerRadius(10)
            }
            .sheet(isPresented: $showLogin) {
                LoginViewControllerWrapper()
            }
        }
        .padding()
    }
}

